
public class Calculation {
	
	public int add(int x, int y) {
		int z = x+y;
		//System.out.println("Result: "+z);
		return z;
	}
	public int add(int x, int y,int z) {
		int result = x+y+z;
		//System.out.println("Result: "+z);
		return result;
	}
	
	public double add(double x, double y) {
		double z = x+y;
		//System.out.println("Result: "+z);
		return z;
	}

}
